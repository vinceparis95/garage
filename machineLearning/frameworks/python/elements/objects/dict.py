
# Creating a dictionary object
# create a dict
Dict = {}
print(Dict)

# create a dict with integer keys
Dict = {1: "plant computer", 2: "soft drone"}
print(Dict)

# create a dict with a dict method
Dict = dict({1: "pco1", 2: "dr01"})
print(Dict)

# with each item in a pair
Dict = dict([(1, "plant breeding"), (2, "soft bots")])
print(Dict)

